package com.sanxia.javaapi.domain;

import java.util.Date;

public class Electric {

    private String number;

    private String switchNo;

    private String time;

    private String electricA;

    private String electricB;

    private String electricC;

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getSwitchNo() {
        return switchNo;
    }

    public void setSwitchNo(String switchNo) {
        this.switchNo = switchNo;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getElectricA() {
        return electricA;
    }

    public void setElectricA(String electricA) {
        this.electricA = electricA;
    }

    public String getElectricB() {
        return electricB;
    }

    public void setElectricB(String electricB) {
        this.electricB = electricB;
    }

    public String getElectricC() {
        return electricC;
    }

    public void setElectricC(String electricC) {
        this.electricC = electricC;
    }
}
